void map(){
  initscr(); //main window start
  setlocale(LC_ALL, "");
  resize_term(200, 400);
  mvprintw(2,15,"Snake Game"); // y,x
  start_color();
  init_pair(1, COLOR_WHITE, COLOR_BLACK); // 글씨색, 배경색
  init_pair(2, COLOR_GREEN, COLOR_BLACK);

  WINDOW *gamewin = newwin(21, 40, 3, 3); //int nlines, int ncols, int begy, int begx
  box(gamewin,0,0);
  wbkgd(gamewin, COLOR_PAIR(1));

  for (int r = 0; r < 21; r++){
    for (int c = 0; c < 40; c++){
      if (map_array[r][c] == 2) {mvwaddch(gamewin,r,c,'+');} //y,x
      else if (map_array[r][c] == 1 && (r == 0 || r == 20)) {mvwaddch(gamewin,r,c,'-');}
      else if (map_array[r][c] == 1) {mvwaddch(gamewin,r,c,'|');}
      else {mvwaddch(gamewin,r,c,' ');}
    }
  }
  wrefresh(gamewin);

  refresh();
  getch();
  delwin(gamewin);
  endwin(); // main window end
}

int main(){
  map();
}
